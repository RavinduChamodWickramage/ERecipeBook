                <div class="services-box">
                    <ul>
	<li>50g butter, melted</li>
	<li>6 slices Helga's Traditional Wholemeal bread, crusts removed and reserved</li>
	<li>6 rashers streaky bacon, cut in half lengthways</li>
	<li>40g baby spinach</li>
	<li>1/3 cup shredded tasty cheese</li>
	<li>8 eggs</li>
	<li>6 eggs</li>
	<li>Chives, snipped, to serves</li>
  </ul>
                </div>
                <div class="head-box">
                    <p>Method</p>
                </div>
                <div class="services-box">
                    <ol>
<li>Preheat oven to 190°C. Brush a 6 hole muffin tin with half the melted butter for the egg cups. Grease and line a baking tray for the bacon dippers</li>
<li>Gently roll each slice of bread flat with a rolling pin and press each slice of bread into the prepared muffin hole. Brush with remaining melted butter</li>
	<li>Wrap a strip of bacon around each reserved crust and place on the lined baking tray. Bake both trays in the oven for 10 minutes</li>
	<li>Remove both from the oven, turn the bacon soldiers over. Place a few spinach leaves into the bread cups, and divide the cheese evenly between them. Gently crack an egg into each cup</li>
	<li>Carefully place both trays back in the oven and bake for another 15 minutes or until the egg is just set and the bacon soldiers are crispy</li>
	<li>Remove from the oven, allow cups to cool in pan for 5 minutes before removing. Serve sprinkled with chives, salt and pepper</li>
  </ol>
