                <div class="services-box">
                    <ul>
	<li>4cups (600g) bread flour</li>
	<li>2 tsp dry active yeast</li>
	<li>tbsp sea salt</li>
	<li>1 ¾ cups (430mls) warm water</li>
	<li>½ cup (125ml) olive oil, plus extra for greasing</li>
	<li>8 eggs</li>
	<li>1 cup (100g) grated cheddar cheese</li>
	<li>150g middle cut bacon, cut into thin strips</li>
	<li>½ small red onion, cut into thin wedges</li>
	<li>3-4 fresh rosemary sprigs</li>
  </ul>
                </div>
                <div class="head-box">
                    <p>Method</p>
                    
                </div>
                <div class="services-box">
                    <ol>
	<li>Place flour, yeast, 2 tsp salt in a large mixing bowl. Add warm water and 2 tbsp of the olive oil. Using clean hands, bring mixture together to form a wet and sticky dough. Add a little more oil to bowl and grease mixing bowl. Turn dough in bowl to coat. Cover and set aside in a warm place until it doubles in size (approx. 1 hour)</li>
	<li>Pour remaining olive oil into a large 30cm x 40cm baking dish and swirl to coat evenly</li>
	<li>Punch dough to expel air and place inside oiled baking dish. Using your fingertips, stretch the dough to the edges. Cover again and set aside in a warm place to puff up, (approx. 30 minutes)</li>
	<li>Arrange oven rack in the middle position of the oven and preheat oven 240°C/220°C (fan-forced)</li>
	<li>Using the base of a small glass dusted in flour, press down into dough to make 8 evenly spaced indents. Open out indents more by pushing the dough back with your fingers. Sprinkle with 2 tsp sea salt and scatter over cheese, bacon, onion and rosemary around the indents</li>
	<li>Bake 15 minutes. Remove from oven and press the same glass into the indents to make them deeper. Crack 1 egg into each indent. Bake for a further 7- 9 minutes or until egg whites are set and yolks runny. Serve warm</li>
  </ol>
